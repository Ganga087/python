#ğŸ”¹ a. Extract numbers from array which are less and greater than a specified number
import numpy as np
arr = np.array([5, 15, 25, 35, 45])

# Extract numbers greater than 20
print("Greater than 20:", arr[arr > 20])  # [25 35 45]
print(arr)
# Extract numbers less than 30
